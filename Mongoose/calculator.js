



function sum(a,b){

    return a+b
}


function subtract(a,b){
    return a-b
}


function multiply(a,b){
    return a*b
}


function modulus(a,b){
    return a%b
}

function divisor(a,b){
    return Math.floor(a/b)
}


module.exports = {sum,subtract,multiply,modulus,divisor}